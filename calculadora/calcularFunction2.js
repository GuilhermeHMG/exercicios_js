
//Armazenando uma função arrow em uma variável

const soma = (a, b) => {
    return a + b;
}
console.log(soma(2, 3));

const subtrai = (a, b) => {
    return a - b;
}
console.log(subtrai(2, 3));

const multiplica = (a, b) => {
    return a * b;
}
console.log(multiplica(2, 3));

const divide = (a, b) => {
    return (a / b).toFixed(2);
}
console.log(divide(2, 3));
