console.log(`Agora, subtraindo de ${num1} o valor de ${num2}, temos ${subtracao}`);
console.log(`Dividindo ${num1} por ${num2}, temos ${divisao.toFixed(2)}`);
console.log(`e multiplicando ${num1} por ${num2} temos ${multi}`);
